    private void btnorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnorderActionPerformed
        // TODO add your handling code here:
        int customer_id = Integer.parseInt(txtid.getText());
        LocalDate date = LocalDate.now(); 
        int qty = Integer.parseInt(txtqty.getText());
        Integer price = Integer.valueOf(tblresults.getValueAt(tblresults.getSelectedRow(), 3).toString());
        Double total_amount = (double) price * (double) qty;
        
        Order order = new Order(customer_id, date, total_amount);
        ProductRepository productrepository = new ProductRepository();
        boolean isOrdered = productrepository.placeOrder(order);
        
        int orderid = productrepository.getOrderId();
        
        if(isOrdered){
            try {
                showSuccessMessage();
                
                // Update stockquantity
                ConnectionClass con = new ConnectionClass();
                Connection connection = con.createConnection();
                int product_id = Integer.parseInt(tblresults.getValueAt(tblresults.getSelectedRow(), 0).toString());
                
                String updateQuery = "UPDATE products SET stock_quantity = stock_quantity - ? WHERE PRODUCT_ID = ?";
                
                PreparedStatement preparedStatement = connection.prepareStatement(updateQuery);
                preparedStatement.setInt(1, qty);
                preparedStatement.setInt(2, product_id);
                preparedStatement.executeUpdate();
                
                
                // Insert into order_details
            String insertQuery = "INSERT INTO orders_details (order_id, product_id, qty, order_price) VALUES (?, ?, ?, ?)";
            
            PreparedStatement insertStatement = connection.prepareStatement(insertQuery);
                insertStatement.setInt(1, orderid);
                insertStatement.setInt(2, product_id);
                insertStatement.setInt(3, qty);

                // Retrieve price of the product
                double total_price = price * qty;
                insertStatement.setDouble(4, total_price);

                insertStatement.executeUpdate();
          
            } catch (SQLException ex) {
                Logger.getLogger(frmproductdetail.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
           showNoSuccessMessage();
         }
        
    }//GEN-LAST:event_btnorderActionPerformed

