    void getproducts() {
        ProductRepository productRepository
                = new ProductRepository();
        ResultSet rs = productRepository.getAllProducts();
        tblResultModel = new DefaultTableModel(new Object[][]{},
                new String[]{"PRODUCT ID", "Name", "description", "price", "Stock Quantity"});
        try {
            while (rs.next()) {
                tblResultModel.addRow(new String[]{
                    rs.getString("product_id"),
                    rs.getString("product_name"),
                    rs.getString("description"),
                    rs.getString("price"),
                    rs.getString("stock_quantity")});
            }
            tblresults.setModel(tblResultModel);
        } catch (SQLException ex) {
            Logger.getLogger(frmcustomerdetail.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
